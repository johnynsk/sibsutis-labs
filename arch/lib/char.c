int * char_plus()
{
    int* bchar = malloc(sizeof(int) * 64);
    for (int x = 0; x < 8; x++) {
        for (int y = 0; y < 8; y++) {
            bchar[x * 8 + y] = 0;
            if (x == 3 || x == 4 || y == 3 || y == 4) {
                bchar[x * 8 + y] = 1;
            }
        }
    }

    return bchar;
}

